    <h1>Prohlášení o přístupnosti</h1>
    <p>
    Báleš Antonín, Ferencei Filip, Boháč Petr, Ondřej Hána (dále jako zprovozovatelé) se zavazují ke zpřístupnění obsahu svých internetových stránek v souladu se zákonem č. 99/2019 Sb., o přístupnosti internetových stránek a mobilních aplikací a o změně zákona č. 365/2000 Sb., o informačních systémech veřejné správy a o změně některých dalších zákonů, ve znění pozdějších předpisů, které provádějí směrnici Evropského parlamentu a Rady (EU) 2016/2102.
        <br>
    Toto prohlášení o přístupnosti se vztahuje na internetové stránky maturitest.petrbohac.eu.
        <br>
    Stav souladu
    Tyto webové stránky jsou částečně v souladu se zákonem č. 99/2019 Sb., ve znění pozdějších předpisů, z důvodu nesouladu nebo výjimek uvedených níže.
        <br>
    Nepřístupný obsah
    Níže uvedený obsah není přístupný:
        <br>
    z důvodu nesouladu se zákonem č. 99/2019 Sb., ve znění pozdějších předpisů;
    Odkazy na náhledových fotografiích ve fotogaleriích nemají textový popis, protože obrázky nemají uvedený alternativní text (kritéria 1.1.1 a 2.4.4 standardu WCAG 2.1). Vzhledem k množství fotografií a kapacitám provozovatele webu by doplnění smysluplného alternativní text ke všem fotografiím způsobilo nepřiměřenou zátěž (podle § 7 zákona)
    jelikož nespadá do oblasti působnosti platných právních předpisů;
    Videa na internetových stránkách neobsahují ani titulky ani audio popis či alternativu (kritéria 1.2.1 a 1.2.2 standardu WCAG 2.1), na základě výjimky podle § 13 odstavce 4 zákona č. 99/2019 Sb.
    Vypracování tohoto prohlášení o přístupnosti
    Toto prohlášení bylo vypracováno dne 17.2.2023.
        <br>
    Prohlášení bylo vypracováno posouzením provedeným třetí stranou – zpracovatelem těchto internetových stránek Mgr. Michaelou Vrankovou.
        <br>
    Při posuzování se sledovala shoda s kritérii uvedenými v Metodickém pokynu k zákonu č. 99/2019 Sb., ve znění pozdějších předpisů a standardu Web Content Accessibility Guidelines – WCAG 2.1.
        <br>
    Zpětná vazba a kontaktní údaje
    Vaše náměty či informace o problémech při zobrazování stránek, stejně jako dotazy, návrhy nebo připomínky týkající se obsahu webu či důvodného podezření, že webová stránka nesplňuje požadavky zákona č. 99/2019 Sb., ve znění pozdějších předpisů, včetně možnosti podat podnět vůči příslušným státním orgánům, můžete psát na adresu: obec@vzorova-lhota.cz
        <br>
    Kontakt na osobu odpovědnou za přístupnost stránek a za zpracování žádostí zaslaných prostřednictvím mechanismu zpětné vazby:
    ferencei@spsejecna.cz
    </p>



